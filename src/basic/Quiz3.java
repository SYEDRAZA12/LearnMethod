package basic;

public class Quiz3 {

    public static void main(String[] args) {
        int day = 5;


        switch (day) {
            case 1:
                System.out.println("Todays Day is Monday");
                break;


            case 2:
                System.out.println("Todays Day is Tuesday");
                break;


            case 3:
                System.out.println("Todays Day is Wednesday");
                break;


            case 4:
                System.out.println("Todays Day is Thursday");
                break;


            case 5:
                System.out.println("Todays Day is Friday");
                break;


            case 6:
                System.out.println("Todays Day is Saturday");
                break;


            case 7:
                System.out.println("Todays Day is Sunday");
                break;

            default:
                System.out.println("You entered a wrong number");


        }


    }
}
